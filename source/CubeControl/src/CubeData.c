void
resetData( const pCubeData_t _pCubeData[] ) {
    uint8_t x, z;
    for(z = 0; z < 8; z++) {
        for(x = 0; x < 8; x++) {
            _pCubeData[z][x].red = 0x00000000;
            _pCubeData[z][x].green = 0x00000000;
            _pCubeData[z][x].blue = 0x00000000;
        }
    }
    
    return;
}

